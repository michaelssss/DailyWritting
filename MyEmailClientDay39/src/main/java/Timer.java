public class Timer {
    public static Integer getSecond() {
        return Integer.valueOf(1000);
    }

    public static Integer getMins() {
        return Integer.valueOf((1000 * 60));
    }

    public static Integer getHours() {
        return Integer.valueOf((1000 * 60 * 60));
    }
}


